package util;

public enum Methods {
    GET(0),
    POST(1),
    PUT(2);

    private Integer name;

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    Methods(Integer name) {
        this.name = name;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getNameString() {
        return name.toString();
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Integer getName() {
        return name;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setName(Integer name) {
        this.setName(name);
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public String toString() {
        String resut = "";
        switch (this.getName()) {

            case 0:
                resut = "GET";
                break;

            case 1:
                resut = "POST";
                break;

            case 2:
                resut = "PUT";
                break;
        }
        return resut;
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public  static Methods getNameByNumber(Integer i){
        return Methods.values()[i];
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}
